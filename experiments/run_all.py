"""Run the full ORACLE evaluation suite and write results/RESULTS.md.

Usage:
    python -m experiments.run_all            # full evaluation
    python -m experiments.run_all --quick    # fast smoke configuration
"""

from __future__ import annotations

import sys
import time
import traceback

from experiments import (exp_validity, exp_recovery, exp_anytime,
                         exp_change, exp_systems, exp_ablations)
from experiments.common import RESULTS_DIR
import os


def _run(name, fn, quick):
    print(f"\n{'='*60}\n  {name}\n{'='*60}")
    t0 = time.time()
    try:
        out = fn(quick=quick)
        print(f"  -> {name} done in {time.time()-t0:.1f}s")
        return out
    except Exception:
        traceback.print_exc()
        print(f"  -> {name} FAILED")
        return None


def main(quick=False):
    t0 = time.time()
    res = {}
    res["validity"] = _run("Validity audit (sec 7.1/5.4)", exp_validity.main, quick)
    res["recovery"] = _run("Graph recovery (sec 7.2)", exp_recovery.main, quick)
    res["anytime"] = _run("Anytime behaviour (sec 7.3)", exp_anytime.main, quick)
    res["change"] = _run("Change detection (sec 7.4)", exp_change.main, quick)
    res["systems"] = _run("Systems (sec 7.5)", exp_systems.main, quick)
    res["ablations"] = _run("Ablations (sec 7.6)", exp_ablations.main, quick)
    write_report(res)
    print(f"\nTOTAL suite time: {time.time()-t0:.1f}s")
    print(f"Report: {os.path.join(RESULTS_DIR, 'RESULTS.md')}")
    return res


def _fmt(x, nd=2):
    if x is None:
        return "n/a"
    if isinstance(x, float):
        return f"{x:.{nd}f}"
    return str(x)


def write_report(res):
    L = []
    L.append("# ORACLE evaluation results\n")
    L.append("Online Anytime-Valid Causal Discovery -- empirical evaluation of "
             "the corrected design in `algorithm.md`.\n")
    L.append("Generated by `python -m experiments.run_all`. All figures in "
             "`results/figures/`.\n")

    # Validity
    v = res.get("validity")
    L.append("\n## 1. Validity audit (sec 7.1 / 5.4) -- the falsification test\n")
    if v:
        L.append("Null stream of mutually independent series (H0: no edges). "
                 "The corrected SKIT betting wealth controls error at every "
                 "stopping time; naive optional stopping (repeated uncorrected "
                 "p-value looks) inflates badly.\n")
        L.append(f"- target `alpha = {v['alpha']}`, K = {v['K']} candidate edges\n")
        L.append("| metric | ORACLE (SKIT) | Naive optional stopping |")
        L.append("|---|---|---|")
        L.append(f"| max per-edge type-I over t | **{_fmt(v['skit_max_per_edge'],3)}** "
                 f"| {_fmt(v['naive_max_per_edge'],3)} |")
        L.append(f"| max FWER over t | **{_fmt(v['skit_max_fwer_ebonf'],3)}** "
                 f"(e-Bonferroni) | {_fmt(v['naive_max_fwer'],3)} (uncorrected) |")
        L.append("\n![validity](figures/validity_fwer.png)\n")
        ok = (v['skit_max_per_edge'] <= v['alpha'] + 0.03 and
              v['skit_max_fwer_ebonf'] <= v['alpha'] + 0.03)
        L.append(f"**Verdict:** SKIT stays at/below alpha for all t "
                 f"({'PASS' if ok else 'CHECK'}); naive does not. The original "
                 f"draft's `exp(HSIC*lambda)` e-value would diverge here.\n")

    # Recovery
    r = res.get("recovery")
    L.append("\n## 2. Graph recovery (sec 7.2)\n")
    if r:
        L.append("Non-Gaussian ANM data from random DAGs, normalised (low "
                 "varsortability). ORACLE online vs batch baselines (checkpoint "
                 "harness). Lower SHD/SID better; higher F1 better.\n")
        for s in r:
            L.append(f"\n### p={s['p']}, {s['kind']}, {s['noise']} noise, "
                     f"density={s['density']} ({s['n_graphs']} graphs, n={s['n']})\n")
            L.append(f"- varsortability raw={_fmt(s['varsortability_raw'])} -> "
                     f"normalised={_fmt(s['varsortability_norm'])}; "
                     f"throughput {_fmt(s['throughput_obs_per_sec'],0)} obs/s\n")
            L.append("| method | SHD | SID | skeleton F1 | orient F1 |")
            L.append("|---|---|---|---|---|")
            for m in ("ORACLE", "PC_fisherz", "PC_kci_window", "GES_BIC"):
                d = s.get(m)
                if d:
                    star = "**" if m == "ORACLE" else ""
                    L.append(f"| {star}{m}{star} | {_fmt(d['shd'],1)} | "
                             f"{_fmt(d['sid'],1)} | {_fmt(d['skeleton_f1'])} | "
                             f"{_fmt(d['orient_f1'])} |")
        L.append("\n![convergence](figures/recovery_convergence.png)\n")

    # Anytime
    a = res.get("anytime")
    L.append("\n## 3. Anytime behaviour (sec 7.3)\n")
    if a:
        L.append(f"On planted DAGs (p={a['p']}, {a['n_graphs']} graphs): median "
                 f"sample-to-detection at "
                 f"{_fmt(a['median_detection_fraction'])} of the stream; mean "
                 f"ORACLE false skeleton edges at end = "
                 f"{_fmt(a['oracle_false_edges_end_mean'])}. Naive optional "
                 f"stopping accumulates false edges as the stream grows.\n")
        L.append("\n![false edges](figures/anytime_false_edges.png)\n")

    # Change
    c = res.get("change")
    L.append("\n## 4. Change detection (sec 7.4)\n")
    if c:
        L.append(f"Piecewise-stationary SCM (p={c['p']}, segment={c['segment_len']}, "
                 f"{c['n_changes']} change-points). Page-CUSUM on edge e-increments; "
                 f"sweep threshold h for the delay/ARL trade-off.\n")
        L.append("| h | mean delay (obs) | mean ARL to false alarm (obs) |")
        L.append("|---|---|---|")
        for h, d, arl in zip(c['h_grid'], c['mean_delay'], c['mean_arl']):
            L.append(f"| {h} | {_fmt(d,0)} | {_fmt(arl,0)} |")
        L.append(f"\n- example planted change-points: {c['example_change_points']}")
        if c.get('example_ruptures_cps'):
            L.append(f"- ruptures (KernelCPD) detected: {c['example_ruptures_cps']}")
        L.append("\n![delay-arl](figures/change_delay_arl.png)\n")

    # Systems
    sysr = res.get("systems")
    L.append("\n## 5. Systems (sec 7.5)\n")
    if sysr:
        L.append("| p | pairs | RFF D | throughput (obs/s) | peak mem (MB) |")
        L.append("|---|---|---|---|---|")
        for row in sysr:
            L.append(f"| {row['p']} | {row['n_pairs']} | {row['n_features']} | "
                     f"{_fmt(row['obs_per_sec'],0)} | {_fmt(row['peak_mem_mb'],1)} |")
        L.append("\n![scaling](figures/systems_scaling.png)\n")

    # Ablations
    ab = res.get("ablations")
    L.append("\n## 6. Ablations (sec 7.6)\n")
    if ab:
        def tbl(title, d, keyname):
            L.append(f"\n**{title}**\n")
            L.append(f"| {keyname} | SHD | SID | skeleton F1 | orient F1 |")
            L.append("|---|---|---|---|---|")
            for kk, vv in d.items():
                L.append(f"| {kk} | {_fmt(vv['shd'],1)} | {_fmt(vv['sid'],1)} | "
                         f"{_fmt(vv['skeleton_f1'])} | {_fmt(vv['orient_f1'])} |")
        if "alpha" in ab:
            tbl("alpha level", ab["alpha"], "alpha")
        if "k" in ab:
            tbl("degree cap k (k=0 is a pairwise dependency graph, not a DAG)",
                ab["k"], "k")
        if "bet" in ab:
            tbl("bet sizing", ab["bet"], "bet")
        if "n_features" in ab:
            tbl("RFF feature count D", ab["n_features"], "D")

    L.append("\n---\n*Reproduce with `conda run -n py313 python -m "
             "experiments.run_all` (set PYTHONPATH to the repo root).*\n")

    path = os.path.join(RESULTS_DIR, "RESULTS.md")
    with open(path, "w") as f:
        f.write("\n".join(L))


if __name__ == "__main__":
    main(quick="--quick" in sys.argv)
